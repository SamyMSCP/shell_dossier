class ComponentTypeBoolEdit extends ComponentGenerator {

	protected static $_dependances = [];
	protected static $_componentName = "component-type-bool-edit";

	private function __construct() { }

	private function __destruct() {}

	// on pourra utiliser config pour redéfinir le component d'edition à utiliser dans un cas ou dans l'autre pour certains component
	protected static function getTemplate($class, $config) {
		$componentClassName = static::getClassName($class);
		$rt = "<div class='$componentClassName component'>";

		if (SHOW_FRAME)
			$rt .= "<div class='debugMsg' >$componentClassName</div>";
		$rt .= "<input type = checkbox v-model='data.value' />";
		$rt .= "{{ (data.value) ? 'oui' : 'non' }}";
		$rt .= "</div>";
		return ($rt);
	}

}
